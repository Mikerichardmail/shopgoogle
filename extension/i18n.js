function applyLanguage(lang) {
    const dict = translations[lang] || translations["en"];
    
    // Elements to translate using textContent
    const els = {
        "trendingDeals": document.querySelector('h4[data-i18n="trendingDeals"]'),
        "btnShowSubmit": document.getElementById('btn-show-submit'),
        "titleSubmit": document.querySelector('h4[data-i18n="titleSubmit"]'),
        "btnSubmit": document.getElementById('btn-submit-coupon'),
        "btnCancel": document.getElementById('btn-cancel-submit'),
        "btnSettings": document.getElementById('btn-options')
    };

    if (els.trendingDeals) els.trendingDeals.textContent = dict["trendingDeals"];
    if (els.btnShowSubmit) els.btnShowSubmit.textContent = dict["btnShowSubmit"];
    if (els.titleSubmit) els.titleSubmit.textContent = dict["titleSubmit"];
    if (els.btnSubmit) els.btnSubmit.textContent = dict["btnSubmit"];
    if (els.btnCancel) els.btnCancel.textContent = dict["btnCancel"];
    if (els.btnSettings) els.btnSettings.title = dict["btnSettings"];

    // Input Placeholders
    const inputs = {
        "searchPlaceholder": document.getElementById('search-input'),
        "subStore": document.getElementById('sub-store'),
        "subCode": document.getElementById('sub-code'),
        "subTitle": document.getElementById('sub-title')
    };

    if (inputs.searchPlaceholder) inputs.searchPlaceholder.placeholder = dict["searchPlaceholder"];
    if (inputs.subStore) inputs.subStore.placeholder = dict["subStore"];
    if (inputs.subCode) inputs.subCode.placeholder = dict["subCode"];
    if (inputs.subTitle) inputs.subTitle.placeholder = dict["subTitle"];
}
